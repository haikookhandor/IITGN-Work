import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import tensorflow as tf
from tensorflow.keras.layers import LSTM, Dense
from tensorflow.keras.models import Sequential
dataset = pd.read_csv('MinimumDailyTemperaturesDataset.csv')
# plt.plot(data['Date'],data['Temp'])
# plt.show()

#TODO : Write here

past_days = 30 # define the number of past days to use to predict the temperature for the next k days
k = 5 # define the number of days to predict in advance
data = dataset['Temp']
# creating input and output sequences
X = []
y = []
for i in range(len(data)-(past_days+k)):
    X.append(data[i:i+past_days])
    y.append(data[i+past_days:i+past_days+k])
X = np.array(X)
y = np.array(y)

X = X.reshape(X.shape[0], X.shape[1], 1) # reshape the input and output sequences for the LSTM layer

# Model architecture
model = Sequential()
model.add(LSTM(64, input_shape=(past_days, 1)))
model.add(Dense(k))

# Model Compilation
model.compile(optimizer='adam', loss='mse')

# Training the model
model.fit(X, y, epochs=20)

# Generating a prediction for the temperature k days in the future based on the last past_days days of temperature data
last_past_days = data[-past_days-k:-k] # get the last past_days days of temperature data
last_past_days = last_past_days.values.reshape(1, past_days, 1)
prediction = model.predict(last_past_days)[0]
true_values = data[-k:] # get the true values for the next k days
print("RMSE error is: ", np.sqrt(np.mean((prediction-true_values)**2)))
print("Predicted temperature for", k, "days in the future:")
plt.plot(dataset['Date'][-k:], prediction, label = 'Predicted')
plt.plot(dataset['Date'][-k:], true_values, label = 'True')
plt.legend()
plt.show()
# print(prediction)
# print(true_values)

# For N - last_past_days number of sequences, generating a future prediction by sliding window approach to predict the entire temperature data
predictions = []
for i in range(past_days, len(data)):
    x = data[i-past_days:i]
    x = x.values.reshape(1, past_days, 1)
    prediction = model.predict(x)[0][0]
    predictions.append(prediction)
plt.plot(dataset['Date'][past_days:], predictions, label = 'Predicted', alpha = 0.8)
plt.plot(dataset['Date'][past_days:], dataset['Temp'][past_days:], label = 'True', alpha = 0.3)
plt.legend()
plt.show()